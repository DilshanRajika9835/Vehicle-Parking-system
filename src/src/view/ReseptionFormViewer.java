    private void CostClculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CostClculateActionPerformed
       String intime=intimetext.getText();
       String outtime=outtimetext.getText();
       String vehicletype=Vehicletypebox.getSelectedItem().toString();
        try {
            double value=VehicleController.CalculateCost(intime,outtime,vehicletype);
            if(value>0){
             paymenttxt.setText(Double.toString(value));
            }else{
            JOptionPane.showMessageDialog(null,"Cost Calculate is Fail","",JOptionPane.WARNING_MESSAGE);
            }
        } catch (ParseException ex) {
                String message=ex.getMessage();
            JOptionPane.showMessageDialog(null, message,"",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_CostClculateActionPerformed

