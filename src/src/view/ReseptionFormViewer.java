    private void SearchidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchidActionPerformed
          String id=Searchid.getText();
        
        try {
            Vehicle vehicle=VehicleController.SearchVehicle(id);
            if(vehicle !=null){
            Searchid.setText(vehicle.getVehicleID());
            Vehicletypebox.setSelectedItem(vehicle.getVehicleType());
            customername.setText(vehicle.getCustomerName());
            intimetext.setText(vehicle.getIntime());
            outtimetext.setText(vehicle.getOuttime());
            paymenttxt.setText(Double.toString(vehicle.getPayments()));
           
            }else{
             JOptionPane.showMessageDialog(null,"Vehicle Not Found","",JOptionPane.WARNING_MESSAGE);
            }
        } catch (ClassNotFoundException | SQLException ex) {
                String message=ex.getMessage();
            JOptionPane.showMessageDialog(null, message,"",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_SearchidActionPerformed

