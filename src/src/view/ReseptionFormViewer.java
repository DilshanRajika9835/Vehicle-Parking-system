    private void UpdatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatebuttonActionPerformed
       if(Searchid.getText().length()>0){ 
        String vehicleid=Searchid.getText();
        String vehicletype=Vehicletypebox.getSelectedItem().toString();
        String custname=customername.getText();
        String intime=intimetext.getText();
        String outtime=outtimetext.getText();
        double payment=Double.parseDouble(paymenttxt.getText());
        Vehicle vehicle=new Vehicle(vehicleid,vehicletype,custname,intime,outtime,payment);
        try {
            boolean update=VehicleController.Updatecustomer(vehicle);
            if(update){
              JOptionPane.showMessageDialog(null,"Save Successfully!");
            }else{
             JOptionPane.showMessageDialog(null,"Save Fail!");
            }
        } catch (ClassNotFoundException | SQLException ex) {
              String message=ex.getMessage();
            JOptionPane.showMessageDialog(null, message,"",JOptionPane.WARNING_MESSAGE);
        }
       }else{
          JOptionPane.showMessageDialog(null,"Enter VehicleID","",JOptionPane.WARNING_MESSAGE);
       }
    
    }//GEN-LAST:event_UpdatebuttonActionPerformed

